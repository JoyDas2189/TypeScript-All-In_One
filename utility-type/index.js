//Omit => Omit removes keys from an object type.
var joy1 = {
    name: 'joy',
    age: 23,
    gender: 'male'
};
console.log(joy1);
// Pick removes all but the specified keys from an object type.
var joy2 = {
    name: 'joy',
    age: 23,
    gender: 'male'
};
console.log(joy2);
